using System.Collections;
using System.Collections.Generic;
using UnityEngine;


[CreateAssetMenu(fileName = "ItemDatabase", menuName = "Inventory/Database")]


public class ItemDatabaseSO : ScriptableObject
{
    public List<ItemSO> items = new List<ItemSO>();           //ItemSo를 리스트로 관리 한다/ 

    //캐싱을 위한 사전
    private Dictionary<int, ItemSO> itemById;                //ID로 아이템 찾기 위한 캐싱
    private Dictionary<string, ItemSO> itemByName;           //이름으로 아이템 찾기'

    public void Initialize()                                 //초기 설정 함수
    {
        itemById = new Dictionary<int, ItemSO>();            //위에[ 선언만 했기 때문에 Dictionary 할당
        itemByName = new Dictionary<string, ItemSO>();

        foreach (var item in items)                          //items 리스트에 선언 되어 있는것을 가지고 Dictionary에 입력한다.
        {
            itemById[item.id] = item;
            itemByName[item.itemName] = item;
        }
    }


    //ID로 아이템 찾기

    public ItemSO GetItemByID(int id)
    {
        if (itemById == null)                   //itemById가 캐싱이 되어 있지 않다면 초기화 한다.
        {
            Initialize();
        }

        if (itemById.TryGetValue(id, out ItemSO item))  //id 값을 찾아서 ItemSO를 리턴 한다.
            return item;

        return null;                                   //없을 경우 NULL
    }

    //이름으로 아이템 찾기
    public ItemSO GetItemByName(string name)           //itemByName가 캐싱이 되어 있지 않다면 초기화 한다.
    {
        if (itemByName == null)
        {
            Initialize();
        }
        if (itemByName.TryGetValue(name, out ItemSO item)) //name 값을 찾아서 ItemSO를 리턴 한다.
            return item;

        return null;
    }
    



    //타입으로 아이템 필터링
    public List<ItemSO> GetItemByType(ItemType type)
    {
        return items.FindAll(item => item.itemType == type);
    }
}

















































































































































































































































































































































































































































































































































































































































































































